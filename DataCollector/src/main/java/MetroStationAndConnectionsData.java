public class MetroStationAndConnectionsData {
    private String line;
    private String name;

    public MetroStationAndConnectionsData(String line, String name) {
        this.line = line;
        this.name = name;
    }

    public String getline() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
