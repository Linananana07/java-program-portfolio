class MetroStationData {
    private String name;
    private String line;

    public MetroStationData(String name, String line) {
        this.name = name;
        this.line = line;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

}

